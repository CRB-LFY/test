package Domain;

public class OldMan {
	private int x;
	private int y;

	public OldMan() {
		super();
		this.x = 50;
		this.y = 100;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
}
