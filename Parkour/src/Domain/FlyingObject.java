package Domain;

public class FlyingObject {
	private int x;
	private int y;

	public FlyingObject() {
		super();
		this.x = 25;
		this.y = 55;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
}
