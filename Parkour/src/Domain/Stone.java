package Domain;

public class Stone {
	private int x;
	private int y;

	public Stone() {
		super();
		this.x =50;
		this.y =50;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
}
